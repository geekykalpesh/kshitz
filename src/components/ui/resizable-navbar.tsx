export const NavbarButton = ({
  href,
  as: Tag = "a",
  children,
  className,
  variant = "primary",
  visible,
  ...props
}: {
  href?: string;
  as?: React.ElementType;
  children: React.ReactNode;
  className?: string;
  variant?: "primary" | "secondary" | "dark" | "gradient";
  visible?: boolean;
} & (
  | React.ComponentPropsWithoutRef<"a">
  | React.ComponentPropsWithoutRef<"button">
)) => {
  const baseStyles =
    "px-5 py-2.5 rounded-full text-xs font-bold uppercase tracking-widest relative cursor-pointer hover:-translate-y-0.5 transition duration-200 inline-block text-center";

  const isScrolled = visible;

  const variantStyles = {
    primary: isScrolled
      ? "bg-black text-white shadow-[0_0_24px_rgba(0,0,0,0.1)]"
      : "bg-white text-black shadow-md",
    secondary: isScrolled
      ? "bg-transparent shadow-none text-black hover:opacity-60"
      : "bg-transparent shadow-none text-white hover:opacity-60",
    dark: "bg-black text-white shadow-[0_0_24px_rgba(34,_42,_53,_0.06)]",
    gradient:
      "bg-gradient-to-b from-blue-500 to-blue-700 text-white shadow-[0px_2px_0px_0px_rgba(255,255,255,0.3)_inset]",
  };

  return (
    <Tag
      href={href || undefined}
      className={cn(baseStyles, variantStyles[variant], className)}
      {...props}
    >
      {children}
    </Tag>
  );
};
